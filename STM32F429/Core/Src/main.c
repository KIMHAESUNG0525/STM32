//TIM11 : 65ms (1us * 65535 ==> about 65ms)

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM11)
	{

	    uint8_t key_value = keypadScan();
	    uint8_t button_value2 = button2_check();
		uint8_t button_value3 = button3_check();

	    if (key_value)
	    {
	        Push(&calc, key_value);
	        Enqueue(&keypad_queue, key_value);
	    }

	    if (button_value2)
	    {
	        Push(&calc, button_value2);
	        Enqueue(&keypad_queue, button_value2);
	    }
	    if (button_value3)
		{
			Push(&calc, button_value3);
			Enqueue(&keypad_queue, button_value3);
		}
	}

}
